
# Example of loop with final range
for i in range(5):
    print(str(i) + " line of total")

print()

# Example of starting and final loop
for i in range(1,5):
    print(str(i) + " line of final range")

print()

# Example of loop with increment
for i in range(1,10,3):
    print(str(i) + " line of increment 3")

print()

# Example of negative increment
for i in range(10,0,-3):
    print(str(i) + " line of negative increment 3")